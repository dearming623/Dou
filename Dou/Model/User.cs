{
    public class User
    {
        private int id;

        public int Id
        {
            get { return id; }
            set { id = value; }
        }
        private string name;

        public string Name
        {
            get { return name; }
            set { name = value; }
        }
        private int sex;

        public int Sex
        {
            get { return sex; }
            set { sex = value; }
        }
        private int year;

        public int Year
        {
            get { return year; }
            set { year = value; }
        }
        private int month;

        public int Month
        {
            get { return month; }
            set { month = value; }
        }
        private int day;

        public int Day
        {
            get { return day; }
            set { day = value; }
        }
        private int hour;

        public int Hour
        {
            get { return hour; }
            set { hour = value; }
        }
        private int minute;

        public int Minute
        {
            get { return minute; }
            set { minute = value; }
        }
    }
}
